/*Veja o ano de nascimento dos clientes e classifique-os como: nascidos antes de 1990 são adultos, 
nascidos entre 1990 e 1995 são jovens e nascidos depois de 1995 são crianças. Liste o nome do cliente 
e esta classificação.*/

SELECT * FROM [dbo].[TABELA DE CLIENTES]

SELECT  TC.NOME, YEAR( TC.[DATA DE NASCIMENTO]) AS ANO ,
CASE WHEN YEAR( TC.[DATA DE NASCIMENTO]) < 1990 THEN 'adultos'
WHEN YEAR( TC.[DATA DE NASCIMENTO]) >= 1990 AND YEAR( TC.[DATA DE NASCIMENTO]) < 1995 THEN 'jovens'
ELSE 'crianças'
END
FROM [dbo].[TABELA DE CLIENTES] TC
ORDER BY ANO 



